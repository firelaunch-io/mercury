export const generateDegenTokenName = (includeNsfw = false) => {
  const useAdjective = Math.random() < 0.8;
  const useSuffix = Math.random() < 0.7;
  const useMultipleAdjectives = Math.random() < 0.3;

  const allAdjectives = includeNsfw
    ? [...adjectives, ...nsfwAdjectives]
    : adjectives;
  const allNouns = includeNsfw ? [...nouns, ...nsfwNouns] : nouns;
  const allSuffixes = includeNsfw ? [...suffixes, ...nsfwSuffixes] : suffixes;

  let name = '';

  if (useAdjective) {
    name +=
      allAdjectives[Math.floor(Math.random() * allAdjectives.length)] + ' ';
    if (useMultipleAdjectives) {
      name +=
        allAdjectives[Math.floor(Math.random() * allAdjectives.length)] + ' ';
    }
  }

  name += allNouns[Math.floor(Math.random() * allNouns.length)];

  if (useSuffix) {
    name += ' ' + allSuffixes[Math.floor(Math.random() * allSuffixes.length)];
  }

  return name.trim();
};

export const generateDegenTokenSymbol = (name: string) => {
  const symbol = name
    .replace(/\s+/g, '')
    .replace(/[aeiou]/gi, '')
    .slice(0, 5)
    .toUpperCase();
  return symbol.length < 3 ? symbol.padEnd(3, 'X') : symbol;
};
